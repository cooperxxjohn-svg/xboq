"""
DOCX Exports — generate Word documents for bid pack deliverables.

Three generators:
    1. generate_rfis_docx          — RFI log table
    2. generate_exclusions_docx    — exclusions + clarifications
    3. generate_bid_summary_docx   — full bid summary mirror

All return bytes (io.BytesIO) for Streamlit download.
Pure functions, no Streamlit dependency.
"""

import io
from datetime import datetime
from typing import Any, Dict, List, Optional

from docx import Document
from docx.shared import Inches, Pt, RGBColor
from docx.enum.table import WD_TABLE_ALIGNMENT
from docx.enum.text import WD_ALIGN_PARAGRAPH


# =============================================================================
# HELPERS
# =============================================================================

def _safe_str(val: Any, max_len: int = 120) -> str:
    """Safely convert to string and truncate."""
    s = str(val) if val is not None else ""
    if len(s) > max_len:
        s = s[:max_len] + "..."
    return s


def _add_heading(doc: Document, text: str, level: int = 1):
    """Add a heading with consistent styling."""
    doc.add_heading(text, level=level)


def _add_table(doc: Document, headers: List[str], rows: List[List[str]]):
    """Add a formatted table with headers and data rows."""
    table = doc.add_table(rows=1 + len(rows), cols=len(headers))
    table.style = 'Light List Accent 1'
    table.alignment = WD_TABLE_ALIGNMENT.LEFT

    # Header row
    hdr_cells = table.rows[0].cells
    for i, h in enumerate(headers):
        hdr_cells[i].text = h

    # Data rows
    for r_idx, row in enumerate(rows):
        cells = table.rows[r_idx + 1].cells
        for c_idx, val in enumerate(row):
            cells[c_idx].text = str(val)

    return table


# =============================================================================
# 1. RFIs DOCX
# =============================================================================

def generate_rfis_docx(
    rfis: List[dict],
    project_id: str = "",
    include_drafts: bool = False,
) -> bytes:
    """
    Generate an RFI log as a DOCX document.

    Args:
        rfis: List of RFI dicts {question, trade, priority, source_page, resolution}.
        project_id: Project identifier for header.
        include_drafts: If False, only approved/sent RFIs are included.

    Returns:
        DOCX content as bytes.
    """
    # Sprint 13: Filter by approval state
    from src.analysis.approval_states import filter_rfis_for_export
    rfis = filter_rfis_for_export(rfis, include_drafts=include_drafts)

    doc = Document()

    # Title
    title = doc.add_heading(f"RFI Log — {project_id or 'Project'}", level=0)
    doc.add_paragraph(
        f"Generated: {datetime.now().strftime('%Y-%m-%d %H:%M')} | "
        f"Total RFIs: {len(rfis)}"
    )
    doc.add_paragraph("")

    if not rfis:
        doc.add_paragraph("No RFIs generated.")
    else:
        # Build table
        headers = ["#", "Trade", "Priority", "Question", "Resolution"]
        rows = []
        for i, rfi in enumerate(rfis, 1):
            rows.append([
                str(i),
                _safe_str(rfi.get("trade", ""), 20).title(),
                _safe_str(rfi.get("priority", ""), 10).upper(),
                _safe_str(rfi.get("question", ""), 100),
                _safe_str(rfi.get("resolution", "Open"), 40),
            ])
        _add_table(doc, headers, rows)

    # Footer
    doc.add_paragraph("")
    footer_para = doc.add_paragraph("Generated by xBOQ Bid Engineer")
    footer_para.runs[0].font.size = Pt(8)
    footer_para.runs[0].font.color.rgb = RGBColor(0x71, 0x80, 0x96)

    buffer = io.BytesIO()
    doc.save(buffer)
    return buffer.getvalue()


# =============================================================================
# 2. EXCLUSIONS & CLARIFICATIONS DOCX
# =============================================================================

def generate_exclusions_docx(
    assumptions: Optional[List[dict]] = None,
    pricing_guidance: Optional[dict] = None,
    project_id: str = "",
) -> bytes:
    """
    Generate an Exclusions & Clarifications DOCX document.

    Combines:
        - Rejected assumptions → Exclusions
        - Accepted assumptions → Clarifications
        - Pricing guidance recommended exclusions/clarifications

    Args:
        assumptions: List of assumption dicts with status field.
        pricing_guidance: Pricing guidance dict from compute_pricing_guidance().
        project_id: Project identifier for header.

    Returns:
        DOCX content as bytes.
    """
    doc = Document()

    # Title
    doc.add_heading(
        f"Exclusions & Clarifications — {project_id or 'Project'}", level=0,
    )
    doc.add_paragraph(
        f"Generated: {datetime.now().strftime('%Y-%m-%d %H:%M')}"
    )
    doc.add_paragraph("")

    assumptions = assumptions or []
    rejected = [a for a in assumptions if a.get("status") == "rejected"]
    accepted = [a for a in assumptions if a.get("status") == "accepted"]

    # Section 1: Exclusions from assumptions
    doc.add_heading("Exclusions", level=1)
    if rejected:
        for a in rejected:
            title = _safe_str(a.get("title", ""), 120)
            cost = a.get("cost_impact")
            cost_str = f" [Cost impact: {cost}]" if cost is not None else ""
            p = doc.add_paragraph(style='List Bullet')
            run = p.add_run(f"{title}{cost_str}")
    else:
        doc.add_paragraph("No exclusions from assumption review.")

    # Pricing guidance exclusions
    pg_excl = (pricing_guidance or {}).get("recommended_exclusions", [])
    if pg_excl:
        doc.add_heading("Additional Recommended Exclusions (from analysis)", level=2)
        for e in pg_excl:
            doc.add_paragraph(_safe_str(e, 150), style='List Bullet')

    doc.add_paragraph("")

    # Section 2: Clarifications from assumptions
    doc.add_heading("Clarifications", level=1)
    if accepted:
        for a in accepted:
            title = _safe_str(a.get("title", ""), 120)
            cost = a.get("cost_impact")
            cost_str = f" [Cost impact: {cost}]" if cost is not None else ""
            p = doc.add_paragraph(style='List Bullet')
            run = p.add_run(f"{title}{cost_str}")
    else:
        doc.add_paragraph("No clarifications from assumption review.")

    # Pricing guidance clarifications
    pg_clar = (pricing_guidance or {}).get("recommended_clarifications", [])
    if pg_clar:
        doc.add_heading("Additional Recommended Clarifications (from analysis)", level=2)
        for c in pg_clar:
            doc.add_paragraph(_safe_str(c, 150), style='List Bullet')

    # Footer
    doc.add_paragraph("")
    footer_para = doc.add_paragraph("Generated by xBOQ Bid Engineer")
    footer_para.runs[0].font.size = Pt(8)
    footer_para.runs[0].font.color.rgb = RGBColor(0x71, 0x80, 0x96)

    buffer = io.BytesIO()
    doc.save(buffer)
    return buffer.getvalue()


